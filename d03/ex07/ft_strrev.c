char	*ft_strrev(char *str)
{
	char	*stor;
	char	tab[10000];
	int		len;

	len = 0;
	stor = tab;
	while (*str != '\0')
	{
		str++;
		len++;
	}
	while (len > 0)
	{
		*stor = *(str - 1);
		str--;
		stor++;
		len--;
	}
	*stor = '\0';
	str = tab;
	return (str);
}
