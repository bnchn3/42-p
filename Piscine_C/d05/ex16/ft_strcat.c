char	*ft_strcat(char *dest, char *src)
{
	int		i;
	int		len;

	i = 0;
	len = 0;
	while (dest[i])
	{
		len++;
		i++;
	}
	i = 0;
	while (src[i])
	{
		dest[len] = src[i];
		i++;
		len++;
	}
	return (dest);
}
