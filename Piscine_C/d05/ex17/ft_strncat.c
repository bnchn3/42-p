char	*ft_strncat(char *dest, char *src, int nb)
{
	int		i;
	int		len;

	i = 0;
	len = 0;
	while (dest[i])
	{
		len++;
		i++;
	}
	i = 0;
	while (nb > 0)
	{
		dest[len] = src[i];
		len++;
		i++;
		nb--;
	}
	dest[len] = '\0';
	return (dest);
}
