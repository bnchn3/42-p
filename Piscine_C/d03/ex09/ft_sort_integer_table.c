void	ft_sort_integer_table(int *tab, int size)
{
	int		i;
	int		*a;
	int		*b;
	int		n;

	i = size;
	a = tab;
	b = a + 1;
	while (i > 0)
	{
		if (*a > *b)
		{
			n = *a;
			*a = *b;
			*b = n;
			i = size;
			a = tab;
			b = a + 1;
		}
		else
		{
			a++;
			b++;
			i--;
		}
	}
}
