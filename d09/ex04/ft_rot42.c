char	*ft_rot42(char *str)
{
	int		i;
	int		j;

	i = 0;
	while (str[i])
	{
		while ((str[i] < 'A' || (str[i] > 'Z' && str[i] < 'a') ||
					str[i] > 'z') && str[i])
			i++;
		if (str[i])
		{
			j = 0;
			while (j < 16)
			{
				str[i]++;
				if ((str[i] > 'Z' && str[i] < 'a') || str[i] > 'z')
					str[i] -= 26;
				j++;
			}
			i++;
		}
	}
	return (str);
}
